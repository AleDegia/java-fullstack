public class printNumbers {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            {
                // for identifica il tipo di ciclo che si verifica un eterminato numero di volte
                // quante volte? quante il valore di i
                // i inizialmente è 1
                // con il punto e virgola posso assegnare un altro valore ad i, in questo caso
                // 10, che rappresenta il numero massimo
                // ad ogni ciclo i aumenta incrementalmente di un unità perchè c'è i++
                System.out.println(i);
            }

        }
    }
}